<?php


var_dump("100" == 100); // Samadengan 
var_dump("100" === 100); // Identik samadengan, false karna tipe data berbeda 
var_dump("100" != 100); // Tidak samadengan
var_dump("100" !== 100); // Identik tidak samadengan, hasil true karna tipe data nya tidak sama
var_dump(90 < 100); // Lebih kecil dari
var_dump(100 <= 100); // Lebih kecil dari atau samadengan
var_dump(200 > 100); // Lebih besar dari
var_dump(100 >= 100); // Lebih besar dari atau samadengan 

